// declaração de variáveis usando let e var
let minhaVariavel; // undefined, pois variáveis let são inicializadas com undefined automaticamente
let outraVariavel; // undefined, mesma razão

// var tem escopo de função
// let e const têm escopo de bloco

// exemplo com var
var a = 10; // 'a' é acessível globalmente no escopo da função ou do arquivo

// exemplo com let
let b = 10; // 'b' é acessível apenas no bloco em que foi declarado

// exemplo com const
const c = 10; // 'c' é uma constante, não pode ser reatribuída, e também tem escopo de bloco

